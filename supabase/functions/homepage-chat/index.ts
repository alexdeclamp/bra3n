
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const openAIApiKey = Deno.env.get('OPENAI_API_KEY');
const SYSTEM_PROMPT = `You are a helpful assistant for Bra3n, an AI-powered knowledge hub. 
About Bra3n:
- Bra3n organizes notes, PDFs, and documents into an AI-powered knowledge hub
- Users can retrieve insights instantly
- Main features: document organization, AI analysis, and collaboration
- The slogan is "Stop Searching. Start Knowing. Take Actions."
- It helps users manage project documents, notes, and images
- It includes AI-powered chat to discuss projects and documents
- It can generate summaries of project content

Be helpful, concise, and focus on explaining how Bra3n can help potential users. 
If you don't know something specific about Bra3n, be honest but focus on the general value proposition.
Always maintain a friendly, helpful tone.`;

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { message } = await req.json();
    
    if (!openAIApiKey) {
      throw new Error('OpenAI API key is not configured');
    }

    console.log('Received message:', message);

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          { role: 'system', content: SYSTEM_PROMPT },
          { role: 'user', content: message }
        ],
        temperature: 0.7,
      }),
    });

    if (!response.ok) {
      const error = await response.text();
      console.error('OpenAI API error:', error);
      throw new Error(`OpenAI API error: ${response.statusText}`);
    }

    const data = await response.json();
    const aiResponse = data.choices[0].message.content;

    return new Response(JSON.stringify({ response: aiResponse }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  } catch (error) {
    console.error('Error in homepage-chat function:', error);
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
